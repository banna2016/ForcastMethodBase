package com.byl.forcast;

public class GroupNumber {

	private String groupNumber;
	private int count;
	
	private String startIssue;
	
	private String stopIssue;
	
	
	
	public String getStartIssue() {
		return startIssue;
	}
	public void setStartIssue(String startIssue) {
		this.startIssue = startIssue;
	}
	public String getStopIssue() {
		return stopIssue;
	}
	public void setStopIssue(String stopIssue) {
		this.stopIssue = stopIssue;
	}
	public String getGroupNumber() {
		return groupNumber;
	}
	public void setGroupNumber(String groupNumber) {
		this.groupNumber = groupNumber;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
